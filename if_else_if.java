public class if_else_if {
    public static void main(String[] args){
        int x = 2;
        int y = 1;
        int z = 5;
        if (x>y && x>z) 
        {
            System.out.println("x is the biggest number!!");
        }
        else if (y>x && y>z)
        {
            System.out.println("y is the biggest number!!");
        }
        else
        {
            System.out.println("z is the biggest number!!");
        }
        
    }
    
}
